    public Heap(int[] array)
    {
        this.array = new int[array.Length][];
        for (int i = 0; i < array.Length; i++)
        {
            this.array[i] = new int[3];
            this.array![i][0] = array[i];
            this.array[i][1] = array[i];
            this.array[i][2] = 1;
        }

        Heapify();
    }

