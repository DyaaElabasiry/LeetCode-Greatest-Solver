public class Solution
{
    public int MinimumSize(int[] nums, int maxOperations)
    {
        Heap heap = new Heap(nums);
        for (int i = 0; i < maxOperations; i++)
        {
            heap.ModifyMax();
        }

        return heap.array[0][0];
    }
}

class Heap
