    private void Heapify()
    {
        for (int i = (array.GetLength(0) / 2) - 1; i >= 0; i--)
        {
            SiftDown(i, array.GetLength(0) - 1);
        }
    }

    private void SiftDown(int start, int end)
    {
        int root = start;
        while ((root * 2) + 1 <= end)
        {
            int child = (root * 2) + 1;
            int toSwap = root;

            if (array[toSwap][0] < array[child][0])
            {
                toSwap = child;
            }

            if (child + 1 <= end && array[toSwap][0] < array[child + 1][0])
            {
                toSwap = child + 1;
            }

            if (toSwap == root)
            {
                return;
            }
            else
            {
                (array[root], array[toSwap]) = (array[toSwap], array[root]);
                root = toSwap;
            }
        }
    }


    public void ModifyMax()
    {
        this.array[0][2]++;
        this.array[0][0] = (int)Math.Ceiling((double)this.array[0][1] / this.array[0][2]);
        int i = 0;
        while (true)
        {
            int parent = array[i][0];
            int left = i * 2 + 1 >= array.Length ? -1 : array[i * 2 + 1][0];
            int right = i * 2 + 2 >= array.Length ? -1 : array[i * 2 + 2][0];
            if (parent >= left && parent >= right)
            {
                return;
            }
            else if (parent <= left && right <= left)
            {
                (array[i], array[i * 2 + 1]) = (array[i * 2 + 1], array[i]);
                i = i * 2 + 1;
            }
            else if(parent <= right && left <= right)
            {
                (array[i], array[i * 2 + 2]) = (array[i * 2 + 2], array[i]);
                i = i * 2 + 2;
            }
            
        }
    }
